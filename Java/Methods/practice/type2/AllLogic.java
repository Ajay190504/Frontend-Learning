package com.type2;

//Type 2: Non-Parameterized with return type Method Practice
// Can have primitive or non-primitive data types as a return type

public class AllLogic {
	
	public byte myAge() {
		byte age = 21;
		return age;
	}
	
	public short myVehicle() {
		short myVehicleNo = 1000;
		return myVehicleNo;
	}
	
	public int mySalary() {
		int mySal = 10000000;
		return mySal;
	}
	
	public long myPhoneNo() {
		long phoneNo = 9022384826l;
		return phoneNo;
	}
	
	public float myScore() {
		float myMarks = 92.60f;
		return myMarks;
	}
	
	public double myPrecision() {
		double myPrec = 92.6000000109;
		return myPrec;
	}
	
	public char firstLetter() {
		char ch  = 'A';
		return ch;
		
	}
	
	public boolean isSmart() {
		boolean isSmrt  = true;
		return isSmrt;
		
	}
	
	public String myName() {
		String name  = "Ajay";
		return name;
		
	}
}
